let a = 10;
let b = 20;

console.log(a == b);  // false (loose equality)
console.log(a === b); // false (strict equality)
console.log(a != b);  // true (loose inequality)
console.log(a !== b); // true (strict inequality)
console.log(a > b);   // false
console.log(a < b);   // true
console.log(a >= b);  // false
console.log(a <= b);  // true
