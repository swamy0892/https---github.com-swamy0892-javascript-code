let count = 5;

console.log(+count);  // 5 (coerces to number, but already a number)
console.log(-count);  // -5 (negates the number)
console.log(++count); // 6 (pre-increment)
console.log(count++); // 6 (post-increment)
console.log(count);   // 7
console.log(--count); // 6 (pre-decrement)
console.log(count--); // 6 (post-decrement)
console.log(count);   // 5
console.log(!true);  // false
